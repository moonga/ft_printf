#include "libft.h"

int		ft_atoi(const char *str)
{
	int		ret;
	int		n;
	int		i;

	ret = 0;
	i = 0;
	n = 1;
	while (str[i] == ' ' || (str[i] > 8 && str[i] < 14))
		i++;
	if (str[i] == '-')
	{
		n = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret = (ret * 10) + (str[i] - '0');
		i++;
	}
	return (ret * n);
}
